def update_po_file(po_file_path, lang_code):
    """
    –û–±–Ω–æ–≤–ª—è–µ—Ç .po —Ñ–∞–π–ª, –∑–∞–ø–æ–ª–Ω—è—è –ø—É—Å—Ç—ã–µ msgstr –∑–Ω–∞—á–µ–Ω–∏—è –∏–∑ —Å–ª–æ–≤–∞—Ä—è TRANSLATIONS
    """
    if not os.path.exists(po_file_path):
        print(f"‚ö†Ô∏è  –§–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω: {po_file_path}")
        return 0
    
    with open(po_file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    updated_count = 0
    lines = content.split('\n')
    i = 0
    new_lines = []
    
    while i < len(lines):
        line = lines[i]
        new_lines.append(line)
        
        # –ò—â–µ–º msgid —Å—Ç—Ä–æ–∫–∏
        if line.startswith('msgid "') and not line.startswith('msgid ""'):
            msgid_match = re.match(r'msgid "(.*)"', line)
            if msgid_match:
                msgid_text = msgid_match.group(1)
                
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–ª–µ–¥—É—é—â—É—é —Å—Ç—Ä–æ–∫—É - —ç—Ç–æ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å msgstr
                if i + 1 < len(lines):
                    next_line = lines[i + 1]
                    
                    # –ï—Å–ª–∏ msgstr –ø—É—Å—Ç–∞—è –∏ —É –Ω–∞—Å –µ—Å—Ç—å –ø–µ—Ä–µ–≤–æ–¥
                    if next_line == 'msgstr ""' and msgid_text in TRANSLATIONS:
                        translation = TRANSLATIONS[msgid_text]
                        
                        # –í—ã–±–∏—Ä–∞–µ–º –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –ø–µ—Ä–µ–≤–æ–¥ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —è–∑—ã–∫–∞
                        if lang_code == 'ky':
                            translated_text = translation[0]  # Kyrgyz
                        elif lang_code == 'ru':
                            translated_text = translation[1]  # Russian
                        else:
                            translated_text = msgid_text  # English (–æ—Ä–∏–≥–∏–Ω–∞–ª)
                        
                        # –ó–∞–º–µ–Ω—è–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É –Ω–∞ –ø–µ—Ä–µ–≤–æ–¥
                        new_lines.append(f'msgstr "{translated_text}"')
                        i += 2  # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º —Å—Ç–∞—Ä—É—é msgstr —Å—Ç—Ä–æ–∫—É
                        updated_count += 1
                        print(f"  ‚úÖ {msgid_text} -> {translated_text}")
                        continue
        
        i += 1
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–Ω—ã–π —Ñ–∞–π–ª
    with open(po_file_path, 'w', encoding='utf-8') as f:
        f.write('\n'.join(new_lines))
    
    return updated_count


